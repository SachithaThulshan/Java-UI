    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String un = jTextField1.getText();
        String pw = jPasswordField1.getText();

        String unv = "Admin";
        String pwv = "Admin";

        if (un.isEmpty() || un.equals("Enter User Name Here!")) {
            JOptionPane.showMessageDialog(rootPane, "PLESE ENTER USER NAME", "WARNING", JOptionPane.WARNING_MESSAGE);
            jTextField1.grabFocus();
            jTextField1.setText("");
            jTextField1.setForeground(Color.BLACK);
        }else if (pw.isEmpty() || pw.equals("jPasswordField1")){
            jPasswordField1.grabFocus();
            jPasswordField1.setText("");
            jPasswordField1.setForeground(Color.BLACK);
        }else{
            if(un.equals(unv) && pw.equals(pwv)){
                DashBoard d = new DashBoard();
                d.setVisible(true);
                this.setVisible(false);

            }else if(un.equals(unv) && pw != pwv){
                JOptionPane.showConfirmDialog(this, "PASSWORD IS INVALID, PLZ ENTER CORRECT PASSWORD!", "ERROR", JOptionPane.ERROR_MESSAGE);
                jPasswordField1.grabFocus();
                jPasswordField1.setText("");
                jPasswordField1.setForeground(Color.BLACK);
            }else if(un != unv && pw.equals(pwv)){
                JOptionPane.showConfirmDialog(this, "USERNAME IS INVALID, PLZ ENTER CORRECT USERNAME!", "ERROR", JOptionPane.ERROR_MESSAGE);
                jTextField1.grabFocus();
                jTextField1.setText("");
                jTextField1.setForeground(Color.BLACK);
            }else{
                JOptionPane.showConfirmDialog(this, "USERNAME & PASSWORD IS INVALID, PLZ ENTER CORRECT USERNAME & PASSWORD!", "ERROR", JOptionPane.ERROR_MESSAGE);
                jTextField1.grabFocus();
                jTextField1.setText("");
                jTextField1.setForeground(Color.BLACK);
                jPasswordField1.setText("");
                jPasswordField1.setForeground(Color.BLACK);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

