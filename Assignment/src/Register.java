    private void txtAddressKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAddressKeyReleased
        char charactor = evt.getKeyChar();
        if(Character.isDigit(charactor)|Character.isLetter(charactor)|evt.getKeyCode() == KeyEvent.VK_BACK_SPACE | evt.getKeyCode() == KeyEvent.VK_SPACE
                | evt.getKeyCode() == KeyEvent.VK_ENTER | evt.getKeyCode() == KeyEvent.VK_SHIFT | evt.getKeyCode() == KeyEvent.VK_2|evt.getKeyCode() == KeyEvent.VK_MINUS
                | evt.getKeyCode() == KeyEvent.VK_PERIOD | evt.getKeyCode() == KeyEvent.VK_LEFT | evt.getKeyCode() == KeyEvent.VK_RIGHT | evt.getKeyCode() == KeyEvent.VK_COMMA
                | evt.getKeyCode() == KeyEvent.VK_SLASH){
            
            txtAddress.setEditable(true);
        }else{
            txtAddress.setEditable(false);
            JOptionPane.showMessageDialog(this, "plz enter Valid character","ERROR",JOptionPane.ERROR_MESSAGE);
            if(evt.getKeyCode() == KeyEvent.VK_ENTER)
                txtAddress.grabFocus();
            txtAddress.setText("");
            txtAddress.grabFocus();
                    
        }
    }//GEN-LAST:event_txtAddressKeyReleased

