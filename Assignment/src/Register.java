    public int contact(){
        String x = txtContact.getText();
        int y = x.length();
        if(y > 8 && y < 12 | y == 12 | y == 13 | y==14){
            if (y == 12 | y == 13 | y==14){
                if(txtContact.getText().contains("+"))
                    return 1;
                else
                    JOptionPane.showMessageDialog(this, "Should be '+' ","Contact No Error",JOptionPane.ERROR_MESSAGE);
            }  
            return 1;
        }else
                JOptionPane.showMessageDialog(this, "Contact Number Is Wrong, Plz Try Again!","Contact Number Error",JOptionPane.ERROR_MESSAGE);
        return 0;
    }
    
    public int birthday(){
        String x = txtDob.getText();
        if(x.contains("/") || x.contains("-")){
            return 1;
        }else
            JOptionPane.showMessageDialog(this, "Plz Fill 'YYYY/MM/DD' This Foramat","Birthday Warning",JOptionPane.WARNING_MESSAGE);
        return 0;
                                     
    }
    public int age(){
        String x = txtAge.getText();
        int y = Integer.parseInt(x);
        if(y > 0 && y < 100){
            return 1;
        }else
            JOptionPane.showMessageDialog(this, "Plz Enter Correct Age!","Age Warning",JOptionPane.WARNING_MESSAGE);
        return 0;
            
    }
    public int nic(){
        String x = txtNIC.getText();
        int y = x.length();
        if(x.trim().matches("^[0-9]{9}[vV]$") || x.trim().matches("^[0-9]{12}")){
            return 1;
        }else
            JOptionPane.showMessageDialog(this, "Plz Enter Valid NIC Number!","NIC Error",JOptionPane.ERROR_MESSAGE);
        return 0;
        
    }
    public int email(){
        String x = txtEmail.getText();
        if(x.contains("@") && x.contains(".")){
            return 1;
        }else
            JOptionPane.showMessageDialog(this, "Plz Enter Valid E-mail!","E-Mail Error",JOptionPane.ERROR_MESSAGE);
        return 0;
    }
    
    
