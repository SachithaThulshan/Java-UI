    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String un = txtUser.getText();
        String pw = pass.getText();

        String unv = "Admin";
        String pwv = "Admin";

        if (un.isEmpty() || un.equals("User Name!")) {
            JOptionPane.showMessageDialog(rootPane, "PLESE ENTER USER NAME", "WARNING", JOptionPane.WARNING_MESSAGE);
            txtUser.grabFocus();
            txtUser.setText("");
            txtUser.setForeground(Color.BLACK);
        }else if (pw.isEmpty() || pw.equals("jPasswordField1")){
            txtUser.grabFocus();
            txtUser.setText("");
            txtUser.setForeground(Color.BLACK);
        }else{
            if(un.equals(unv) && pw.equals(pwv)){
                //                Home H = new Home();
                //                H.setVisible(true);
                //                this.setVisible(false);
                JOptionPane.showMessageDialog(rootPane, "Loged!");

            }else if(un.equals(unv) && pw != pwv){
                JOptionPane.showConfirmDialog(this, "PASSWORD IS INVALID, PLZ ENTER CORRECT PASSWORD!", "ERROR", JOptionPane.ERROR_MESSAGE);
                pass.grabFocus();
                pass.setText("");
                pass.setForeground(Color.BLACK);
            }else if(un != unv && pw.equals(pwv)){
                JOptionPane.showConfirmDialog(this, "USERNAME IS INVALID, PLZ ENTER CORRECT USERNAME!", "ERROR", JOptionPane.ERROR_MESSAGE);
                txtUser.grabFocus();
                txtUser.setText("");
                txtUser.setForeground(Color.BLACK);
            }else{
                JOptionPane.showConfirmDialog(this, "USERNAME & PASSWORD IS INVALID, PLZ ENTER CORRECT USERNAME & PASSWORD!", "ERROR", JOptionPane.ERROR_MESSAGE);
                txtUser.grabFocus();
                txtUser.setText("");
                txtUser.setForeground(Color.BLACK);
                pass.setText("");
                pass.setForeground(Color.BLACK);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

