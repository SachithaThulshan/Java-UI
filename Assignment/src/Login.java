    private void passKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passKeyReleased
        char letter = evt.getKeyChar();
        if(Character.isLetter(letter) | Character.isDigit(letter) | evt.getKeyCode() == KeyEvent.VK_ENTER
            | evt.getKeyCode() == KeyEvent.VK_BACK_SPACE | evt.getKeyCode() == KeyEvent.VK_RIGHT | evt.getKeyCode() == KeyEvent.VK_LEFT
            | evt.getKeyCode() == KeyEvent.VK_UNDERSCORE | evt.getKeyCode() == KeyEvent.VK_SHIFT | evt.getKeyCode() == KeyEvent.VK_CAPS_LOCK
            | evt.getKeyCode() == KeyEvent.VK_DELETE | evt.getKeyCode() == KeyEvent.VK_EQUALS | evt.getKeyCode() == KeyEvent.VK_1
            | evt.getKeyCode() == KeyEvent.VK_2 | evt.getKeyCode() == KeyEvent.VK_3 | evt.getKeyCode() == KeyEvent.VK_4 | evt.getKeyCode() == KeyEvent.VK_5
            | evt.getKeyCode() == KeyEvent.VK_6| evt.getKeyCode() == KeyEvent.VK_7| evt.getKeyCode() == KeyEvent.VK_8| evt.getKeyCode() == KeyEvent.VK_PERIOD){

            txtUser.setEditable(true);

        }else{
            txtUser.setEditable(false);
            JOptionPane.showMessageDialog(rootPane, "PLEASE ENTER VALID CHARACTERS!", "ERROR", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_passKeyReleased

